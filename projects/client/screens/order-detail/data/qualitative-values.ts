/** 内存缓存：从 AsyncStorage 加载的同步数据 */
let _cachedValueMap: Record<string, string> | null = null;

/** 从 AsyncStorage 预加载检测值映射表到内存缓存 */
export async function loadValueMapFromStorage(): Promise<void> {
  _cachedValueMap = await loadInspectValueMapFromStorage();
}

/** 获取当前内存中的映射表（仅供调试用） */
export function getCachedValueMap(): Record<string, string> | null {
  return _cachedValueMap;
}

/** 根据编码查找检验值内容（优先使用同步数据，其次硬编码兜底） */
export function getQualitativeText(code: string | undefined): string {
  if (!code) return '';
  // 1. 优先使用从金蝶同步的最新数据
  if (_cachedValueMap && code in _cachedValueMap) {
    return _cachedValueMap[code];
  }
  // 2. 回退到硬编码默认表
  return QUALITATIVE_VALUE_MAP[code] || code;
}

/** 检验值选项列表（供选择器使用，优先使用同步数据） */
export function getQualitativeOptions(): Array<{ code: string; text: string }> {
  // 优先使用同步数据
  if (_cachedValueMap && Object.keys(_cachedValueMap).length > 0) {
    return Object.entries(_cachedValueMap).map(([code, text]) => ({ code, text }));
  }
  // 回退到硬编码默认表
  return Object.entries(QUALITATIVE_VALUE_MAP).map(([code, text]) => ({ code, text }));
}
